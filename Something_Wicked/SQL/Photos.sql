CREATE TABLE Photos
(
	ID VARCHAR(50) PRIMARY KEY,
	Title VARCHAR(50) NOT NULL,
	Thumbnail VARCHAR(50) NOT NULL,
)



CREATE PROC GetPhotos
AS
BEGIN
	SET NOCOUNT ON
	SELECT ID, Title, Thumbnail
	FROM Photos
	ORDER BY ID DESC
END





CREATE PROC GetPhotosTitle
(
	@ID VARCHAR(50)
)
AS
BEGIN
	SET NOCOUNT ON
	SELECT Title FROM Photos
	WHERE ID = @ID
END
